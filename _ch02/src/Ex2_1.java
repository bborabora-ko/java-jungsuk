
public class Ex2_1 {

	public static void main(String[] args) {
		System.out.print("5+3"); //5+3 print() - 출력 후 줄바꿈x
		System.out.println("5+3"); //5+3 println() - 출력 후 줄바꿈o
		System.out.println(5+3); //8
		System.out.println(5-3); //2
		System.out.println(5*3); //15
		System.out.println(5/3); //1
		System.out.println(5/3.0); //1.3333333
		
	}

}
